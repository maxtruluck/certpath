'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';

const tabs = [
  { href: '/dashboard', label: 'Home', icon: HomeIcon },
  { href: '/career', label: 'Career', icon: CareerIcon },
  { href: '/certifications', label: 'Certs', icon: CertIcon },
  { href: '/profile', label: 'Profile', icon: ProfileIcon },
];

export function BottomNav() {
  const pathname = usePathname();

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 bg-white/90 backdrop-blur-lg border-t-2 border-cp-border pb-safe">
      <div className="max-w-lg mx-auto flex items-center justify-around h-16 px-2">
        {tabs.map((tab) => {
          const isActive = pathname.startsWith(tab.href);
          return (
            <Link
              key={tab.href}
              href={tab.href}
              className={`relative flex flex-col items-center gap-0.5 px-4 py-1.5 rounded-xl transition-all active:scale-90 ${
                isActive ? 'text-cp-green' : 'text-cp-text-muted hover:text-cp-text-secondary'
              }`}
            >
              {isActive && (
                <div className="absolute -top-1.5 w-8 h-1 rounded-full bg-cp-green" />
              )}
              <tab.icon active={isActive} />
              <span className={`text-[10px] font-extrabold tracking-wider ${isActive ? '' : 'font-bold'}`}>{tab.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}

